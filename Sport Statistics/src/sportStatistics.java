public class sportStatistics
{
	private int YourTotalPoints=0, opponentsTotalPoints=0;
	public int getYourTotalPoints()
	{
		return YourTotalPoints;
	}
	public void setYourTotalPoints(int yourTotalPoints)
	{
		YourTotalPoints = yourTotalPoints;
	}
	public int getOpponentsTotalPoints()
	{
		return opponentsTotalPoints;
	}
	public void setOpponentsTotalPoints(int opponentsTotalPoints)
	{
		this.opponentsTotalPoints = opponentsTotalPoints;
	}
}